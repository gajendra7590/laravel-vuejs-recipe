  },
  methods: {
    getAuthorsDetail(id) {
      let _this = this;
      _this.$store.dispatch("authorsDetail",{id : id})
      .then(function(result){
        alert(result)
        if(result.length > 0){ 
          _this.authorDetail = result;
        }else{ 
          _this.$toastr.e('Opps! Invalid Request');
          _this.$router.push('/authors');
        } 
